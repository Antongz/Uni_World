var map;

$(document).ready(function() {
    $("content").hide();
    
    $("#listings").scroll(function() {
        $("#hotel").hide();
        $("#map").show();
   });
    
    $("#infotab").click(function() {
        $("#hoteltabs").html("<p>Info</p>");
    });
    
    $("#reviewstab").click(function() {
        $("#hoteltabs").html("<p>Reviews</p");
    });
    
    $("#maptab").click(function() {
        $("#hoteltabs").html('<iframe width="100%" height="15%" frameborder="0" src="https://www.google.com/maps/embed/v1/view?key=AIzaSyBfJ05x4t-D9qXigaFXBqpgB4Ad7OMGuJk &center=-33.8569,151.2152 &zoom=15" allowfullscreen></iframe>');
    });
    
    var applicablerooms = [];
    
    var terms;
    var markers = [];
    $("#search").keyup(function() {
        for (var i = 0; i < markers.length; i++) {
            markers[i].setMap(null);
        }
        markers = [];
        
        terms = $("#search").val();

        var i = 0;
        applicablerooms = [];
        for (i in data.rooms) {
            if (data.rooms[i]["Location (City, County)"].indexOf(terms) != -1) {
                applicablerooms[applicablerooms.length] = data.rooms[i];
            }
        }
        
        i = 0;
        $("#listings").empty();
        for (i in applicablerooms) {
            $("#listings").append("<li class='listing'><img class='listingimg' src='" + applicablerooms[i]["Image file name"] + "'><p class='listingprice'>$" + applicablerooms[i]["Price per Night"] + "<p></li>");
            
            $(".listing").click(function() {
                $("#map").hide();
                $("#hotel").show();
            });
            
            var lng = applicablerooms[i]["latitude - google coordinates"];
            var lat = applicablerooms[i]["longitude - google coordinates"];
            
            var marker = new google.maps.Marker({
                position: {lat: lat, lng: lng},
                map: map
            });
            
            markers.push(marker);
        }
    });
});

function initMap() {
    var Australia = {lat: -25.363, lng: 131.044};

    map = new google.maps.Map(document.getElementById('map'), {
          zoom: 4,
          center: Australia
    });
}

